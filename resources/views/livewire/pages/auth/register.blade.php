<script>
    const togglePassword = document.querySelector('#togglePassword');
    const password = document.querySelector('#password');

    const togglePasswordConfirm = document.querySelector('#togglePasswordConfirm');
    const password_confirmation = document.querySelector('#password_confirmation');

    togglePassword.addEventListener('click', function(e) {
        // toggle the type attribute
        const type = password.getAttribute('type') === 'password' ? 'text' : 'password';
        password.setAttribute('type', type);
        // toggle the eye slash icon
        //this.classList.toggle('hidden');
        togglePassword.querySelector('#open-eye').classList.toggle('hidden');
        togglePassword.querySelector('#close-eye').classList.toggle('hidden');
    });

    togglePasswordConfirm.addEventListener('click', function(e) {
        const type = password_confirmation.getAttribute('type') === 'password' ? 'text' : 'password';
        password_confirmation.setAttribute('type', type);

        togglePasswordConfirm.querySelector('#open-eye').classList.toggle('hidden');
        togglePasswordConfirm.querySelector('#close-eye').classList.toggle('hidden');

    });
    
</script>
